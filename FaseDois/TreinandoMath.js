// Gere um número aleatório de 1 até 6.
var numAleatorio = Math.trunc(Math.random() * 6 + 1);
console.log(numAleatorio);

// Math.trunc() - remove as casas decimais e também converter para integer.
// Math.floor() - zera as casas decimais.
// Math.round() - retorna o valor arrendodado para o valor inteiro mais próximo.
// Math.pow(x,y) - retorna a base x elevada à potência y do expoente, ou seja, x^y.
// Math.SQRT2 - retorna a raiz quadrada de 2.